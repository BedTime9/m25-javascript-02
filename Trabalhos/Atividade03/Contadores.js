    // Laço 'for'

    console.log("Laço for")
    for(i = 1; i < 11; i++) {
        console.log(`Valor de i(for): [${i}]`);
    }
    console.log('');

    // Laço 'while'

    console.log("Laço while")
    i = 1;
    while(i < 11) {
        console.log(`Valor de i(while): [${i}]`);
        i++;
    }
    console.log('');

    // Laço 'do...while'

    console.log("Laço do-while")
    i = 1;

    do {
        console.log(`Valor de i(do-while): [${i}]`)
        i++
    } while(i < 11);